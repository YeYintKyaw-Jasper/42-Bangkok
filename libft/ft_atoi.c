#include "libft.h"

int	ft_atoi(const char *nptr)
{
	long	res;
	int		neg;

	res = 0;
	neg = 0;
	while (*nptr == ' ' || (*nptr >= 9 && *nptr <= 13))
		nptr++;
	while (*nptr == '+' || *nptr == '-')
	{
		if (neg * neg == 1)
			return (0);
		if (*nptr == '+')
			neg = 1;
		else
			neg = -1;
		nptr++;
	}
	while (*nptr >= '0' && *nptr <= '9')
	{
		res = (res * 10) + (*nptr - '0');
		nptr++;
	}
	if (neg == 0)
		neg = 1;
	return (res * neg);
}
